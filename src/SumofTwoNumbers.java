public class SumofTwoNumbers{

  
  public static void main(String[] args) {
   
    // Variable Declaration
    
     int intNum, flttoint;
   
     float fltNum;
   
     int sum;
        

      
    //Initialisation
  
      intNum=10;
    
      fltNum=10.5f;
        
 
    
    //Typecasting
    
      flttoint=(int) fltNum;
  
    
    //sum calculation
 
       sum=intNum + flttoint;
    

    System.out.println("The sum of two number is : " + sum);
    
  
  }

} 